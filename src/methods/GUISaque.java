    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Float valor = Float.parseFloat(this.jTF_valor.getText());
        String senha = this.jTF_senha.getText();
        UUID numConta = UUID.fromString(this.jTF_numConta.getText());
        
        this.client.getContas().forEach(conta -> {
            if(conta.getNumeroConta().equals(numConta)){
                this.conta.saqueConta(valor, senha);
            }
        });
    }//GEN-LAST:event_jButton1ActionPerformed

