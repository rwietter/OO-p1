    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        ArrayList<ClientRegistration> data = this.database.getClient();
        
        String[] nomeColunas = {
            "Nome", 
            "Endere√ßo", 
            "Telefone", 
            "Renda", 
            "Situacao",
            "CPF"
        };
        
        String[][] valoresLinhas = new String[data.size()][6];
        
        System.out.println(data.size());
        
        for(int i = 0; i < data.size(); i++){
            valoresLinhas[i][0] = data.get(i).getNome();
            valoresLinhas[i][1] = data.get(i).getEndereco();
            valoresLinhas[i][2] = data.get(i).getTelefone();
            valoresLinhas[i][3] = Float.toString(data.get(i).getRenda());
            valoresLinhas[i][4] = Boolean.toString(data.get(i).getSituacao());
            valoresLinhas[i][5] = data.get(i).getCpf();
        }
        
        DefaultTableModel tableModel = new DefaultTableModel(valoresLinhas, nomeColunas);
        
        this.jT_Clientes.setModel(tableModel);
    }//GEN-LAST:event_formWindowActivated

